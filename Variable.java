// Variables in Java

// Variables - The name of a memory block to store data is called variable

public class Variable{
    public static void main (String args[]){
        int a = 10; // Variable is 'a' storing number 10
        int b = 15; // Variable is 'b' storing number 15
        System.out.println(a);
        System.out.println(b);
        
        String name = "Shraddha Kappor";
        System.out.println(name);

    }
}