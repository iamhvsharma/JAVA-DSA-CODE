public class SwitchStatement{
    public static void main(String args[]){
        int name = 1;
        switch(name) {
            case 1: System.out.println("Harsh");
            break;
            case 2: System.out.println("Sam");
            break;
            case 3: System.out.println("Don");
            break;
            default: System.out.println("No name");
            break;
        }
    }
}