// DataTypes in Java

public class DataTypes{
    public static void main(String args[]) {

        byte b = 8;
        char ch = 'a';
        boolean var = false; // Only True and False can be stored
        double price = 2.2; // Used to store Decimal numbers
        int number = 12; // Used to store integer 
        // long - use to store very long numbers at the place of int
        // double - used to store very long decimal numbers at th place of float
        short n = 123; // Not used too much


        System.out.println(b);
        System.out.println(ch);
        System.out.println(var);
        System.out.println(price);
        System.out.println(number);
        System.out.println(n);






    }
}