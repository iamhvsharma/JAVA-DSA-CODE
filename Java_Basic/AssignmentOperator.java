public class AssignmentOperator{
    public static void main(String args[]){
        int A = 10;
        int B = 15;
        int C = 5;
        int D = 20;
        int E = 30;


        // A = A + 10;
        A+=10;
        System.out.println(A);

        // B = B - 10;
        B-=10;
        System.out.println(B);

        // C = C * 10;
        C *= 10;
        System.out.println(C);

        // D = D / 10;
        D/=10;
        System.out.println(D);

        // E = E % 10;
        E%=10;
        System.out.println(E);




    }
}